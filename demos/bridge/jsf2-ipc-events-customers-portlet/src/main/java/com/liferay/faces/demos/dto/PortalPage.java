package com.liferay.faces.demos.dto;

/**
 * @author  Neil Griffin
 */
public class PortalPage {

	// Private Data Members
	private long id;
	private String name;

	public PortalPage(long id, String name) {
		this.id = id;
		this.name = name;
	}

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
